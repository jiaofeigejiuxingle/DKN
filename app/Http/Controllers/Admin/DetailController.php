     */
    public function store(Request $request)
    {
        $data = $request->except("_token");
//        dd($data);
        //数据入库
        if (false != $insertID = DB::table("detail")->insertGetId($data))
        {
            return redirect("/tips")->with(["info" => "Add Ok ！ID:".$insertID, "url" => "/Admin/detail"]);
        } else
        {
            return redirect("/tips")->with(["info" => "Add Failed ！", "url" => "/Admin/detail"]);
        }
    }

